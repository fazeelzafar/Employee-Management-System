    private void btnAnnouncementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnnouncementActionPerformed
        String Name = txtName.getText();
        String Announcement = txtAnnouncement.getText();
        boolean access = false;
        AdminOperations Op = new AdminOperations();
        try {
            access = Op.addAnnounce(Name, Announcement);
            if (access) {
                JOptionPane.showMessageDialog(null, "Announcement posted!");
            } else {
                JOptionPane.showMessageDialog(null, "Error");
            }
        } catch (Exception ex) {
            Logger.getLogger(ApplyForLeave.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnAnnouncementActionPerformed
